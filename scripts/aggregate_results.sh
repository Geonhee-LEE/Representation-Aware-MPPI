#!/usr/bin/env bash
# Aggregate results/*.tsv into a single RESULTS.md at repo root.
# Adapted from Geonhee-LEE/learning_mppi RESULTS.md aggregator pattern (BSD/MIT-style).
# Idempotent; safe to run from cron or the executor's final push step.
set -euo pipefail

REPO="$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)"
cd "${REPO}"

OUT="${REPO}/RESULTS.md"

# Resolve PR # / state per autoresearch/<slug> branch.
# Output: tab-separated "<slug>\t<number>\t<state>\t<url>" lines into ${PR_INFO}.
# Empty file (or missing gh) => python falls back to "(no PR)".
PR_INFO="$(mktemp)"
trap 'rm -f "${PR_INFO}"' EXIT
if command -v gh >/dev/null 2>&1 && gh auth status >/dev/null 2>&1; then
  for tsv in "${REPO}/results"/*.tsv; do
    [ -e "${tsv}" ] || continue
    slug="$(basename "${tsv}" .tsv)"
    [ "${slug}" = "README" ] && continue
    info=$(gh pr list --head "autoresearch/${slug}" --state all \
      --json number,state,url --jq '.[0] // empty' 2>/dev/null || true)
    [ -z "${info}" ] && continue
    num=$(printf '%s' "${info}" | python3 -c 'import json,sys;d=json.load(sys.stdin);print(d.get("number",""))' 2>/dev/null || true)
    state=$(printf '%s' "${info}" | python3 -c 'import json,sys;d=json.load(sys.stdin);print(d.get("state",""))' 2>/dev/null || true)
    url=$(printf '%s' "${info}" | python3 -c 'import json,sys;d=json.load(sys.stdin);print(d.get("url",""))' 2>/dev/null || true)
    [ -n "${num}" ] && printf '%s\t%s\t%s\t%s\n' "${slug}" "${num}" "${state}" "${url}" >>"${PR_INFO}"
  done
fi

python3 - "${REPO}" "${OUT}" "${PR_INFO}" <<'PY'
"""Render results/*.tsv -> RESULTS.md.

- One section per TSV (sorted by mtime, newest first).
- Markdown table for each TSV (header row required).
- Preamble: timestamp, total experiments, status counts.
"""
from __future__ import annotations

import datetime as dt
import os
import sys
from pathlib import Path

repo = Path(sys.argv[1])
out_path = Path(sys.argv[2])
pr_info_path = Path(sys.argv[3]) if len(sys.argv) > 3 else None
results_dir = repo / "results"

pr_by_slug: dict[str, tuple[str, str, str]] = {}
if pr_info_path and pr_info_path.is_file():
    for line in pr_info_path.read_text().splitlines():
        parts = line.split("\t")
        if len(parts) == 4:
            slug, num, state, url = parts
            pr_by_slug[slug] = (num, state, url)

EXCLUDE = {".gitkeep", "README.md"}
STATUS_COUNTS = {"keep": 0, "discard": 0, "crash": 0, "in_progress": 0}


def tsv_files() -> list[Path]:
    if not results_dir.is_dir():
        return []
    files = [
        p for p in results_dir.iterdir()
        if p.is_file() and p.suffix == ".tsv" and p.name not in EXCLUDE
    ]
    files.sort(key=lambda p: p.stat().st_mtime, reverse=True)
    return files


def render_table(rows: list[list[str]]) -> str:
    if not rows:
        return "_(no rows yet)_"
    header = rows[0]
    body = rows[1:]
    pipe = lambda r: "| " + " | ".join(c.replace("|", r"\|") for c in r) + " |"
    sep = "|" + "|".join(["---"] * len(header)) + "|"
    out = [pipe(header), sep]
    out.extend(pipe(r + [""] * (len(header) - len(r))) for r in body)
    return "\n".join(out)


def parse_tsv(path: Path) -> tuple[list[list[str]], int]:
    rows: list[list[str]] = []
    total = 0
    with path.open() as fh:
        for line in fh:
            line = line.rstrip("\n")
            if not line:
                continue
            cols = line.split("\t")
            rows.append(cols)
        if rows:
            for r in rows[1:]:
                total += 1
                if len(r) >= 4:
                    s = r[3].strip()
                    if s in STATUS_COUNTS:
                        STATUS_COUNTS[s] += 1
    return rows, total


files = tsv_files()
sections: list[str] = []
total_experiments = 0

for f in files:
    rows, n = parse_tsv(f)
    total_experiments += n
    slug = f.stem
    pr = pr_by_slug.get(slug)
    if pr:
        num, state, url = pr
        pr_label = f"PR [#{num}]({url}) · {state.lower()}" if url else f"PR #{num} · {state.lower()}"
    else:
        pr_label = "no PR"
    sections.append(
        f"## {slug}\n\n_{pr_label}_\n\nSource: `results/{f.name}`\n\n{render_table(rows)}\n"
    )

now = dt.datetime.now(dt.timezone(dt.timedelta(hours=9))).strftime("%Y-%m-%d %H:%M:%S %z")
preamble = [
    "# RESULTS",
    "",
    "_Auto-generated by `scripts/aggregate_results.sh` - do not edit by hand._",
    "",
    f"- Last update: `{now}`",
    f"- TSV files: **{len(files)}**",
    f"- Total experiment rows: **{total_experiments}**",
    "- Status counts: " + ", ".join(f"`{k}`={v}" for k, v in STATUS_COUNTS.items()),
    "",
    "Sections sorted by most recent TSV mtime (newest first).",
    "",
    "---",
    "",
]

content = "\n".join(preamble) + "\n\n".join(sections) + ("\n" if sections else "_No result TSVs yet._\n")
out_path.write_text(content)
print(f"wrote {out_path} ({len(files)} sections, {total_experiments} rows)")
PY
